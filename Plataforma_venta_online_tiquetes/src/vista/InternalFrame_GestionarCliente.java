// metodo para mostrar todas las clientes
    private void cargartablaClientes() {
        Connection cn = conexion.Conexion.conectar();

        DefaultTableModel model = new DefaultTableModel();
        String sql = "SELECT * FROM cliente;";
        Statement st;
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            InternalFrame_GestionarCliente.tabel_GestionClientes = new JTable(model);
            InternalFrame_GestionarCliente.jScrollPane1.setViewportView(InternalFrame_GestionarCliente.tabel_GestionClientes);

            model.addColumn("Id_Cliente");
            model.addColumn("Nombre");
            model.addColumn("Apellido");
            model.addColumn("Documento");
            model.addColumn("Correo");
            model.addColumn("Direccion");
            model.addColumn("Telefono");

            while (rs.next()) {

                Object fila[] = new Object[7];

                for (int i = 0; i < 7; i++) {
                    fila[i] = rs.getObject(i + 1);

                }
                model.addRow(fila);

            }
            cn.close();

        } catch (SQLException e) {

            System.out.println("Error al llenar la tabla Cliente" + e);
        }

        tabel_GestionClientes.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = tabel_GestionClientes.rowAtPoint(e.getPoint());
                int columna_point = 0;

                if (fila_point > -1) {
                    idCliente = (int) model.getValueAt(fila_point, columna_point);
                    EnviarDatosLineaSeleccionada(idCliente);
                }
            }
        });

    }

    private void EnviarDatosLineaSeleccionada(int idCliente) {
        try {
            Connection cno = conexion.Conexion.conectar();
            PreparedStatement pst = cno.prepareStatement("SELECT * FROM cliente WHERE id_cliente = '" + idCliente + "'");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {

                txt_idCliente.setText(rs.getString("Id_Cliente"));
                txt_nombre_Cliente.setText(rs.getString("Nombre"));
                txt_apellido_Cliente.setText(rs.getString("Apellido"));
                txt_documentoIdentidad_Cliente.setText(rs.getString("Documento"));
                txt_correo_Cliente.setText(rs.getString("Correo"));
                txt_dirrecion_Cliente.setText(rs.getString("Direccion"));
                txt_telefonoCliente.setText(rs.getString("Telefono"));

            }
            cno.close();

        } catch (SQLException e) {

            System.err.println("Error cargar categoria" + e);
        }
    }

