    private void botton_actualizar_datosClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botton_actualizar_datosClienteActionPerformed
        if (!txt_idCliente.getText().isEmpty()) {
            Cliente cliente = new Cliente();
            Ctrl_Cliente controcliente = new Ctrl_Cliente();

            cliente.setIdCliente(Integer.parseInt(txt_idCliente.getText().trim()));
            cliente.setNombre(txt_nombre_Cliente.getText().trim());
            cliente.setApellido(txt_apellido_Cliente.getText().trim());
            cliente.setDocumento(txt_documentoIdentidad_Cliente.getText().trim());
            cliente.setCorreo(txt_correo_Cliente.getText().trim());
            cliente.setTelefono(txt_telefonoCliente.getText().trim());
            cliente.setDireccion(txt_dirrecion_Cliente.getText().trim());
            if (controcliente.actualizar(cliente, idCliente)) {
                JOptionPane.showMessageDialog(null, "Cliente modificado con éxito");
                txt_idCliente.setText("");
                txt_nombre_Cliente.setText("");
                txt_apellido_Cliente.setText("");
                txt_dirrecion_Cliente.setText("");
                txt_documentoIdentidad_Cliente.setText("");
                txt_correo_Cliente.setText("");
                txt_telefonoCliente.setText("");
                cargartablaClientes();
            } else {
                JOptionPane.showMessageDialog(null, "El Cliente no se actualizó");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una cliente");
        }
    }//GEN-LAST:event_botton_actualizar_datosClienteActionPerformed

