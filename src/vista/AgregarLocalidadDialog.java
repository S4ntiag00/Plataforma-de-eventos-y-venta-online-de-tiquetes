    private void btonAgregarLocalidadDialogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btonAgregarLocalidadDialogActionPerformed
        // Obtener los valores ingresados por el usuario
        String nombreLocalidad = txtNombreLocalidad.getText().trim();
        int capacidad = Integer.parseInt(txtEspaciosDisponibles.getText().trim());
        float precio = Float.parseFloat(jTextField3.getText().trim());

        // Validar los valores ingresados
        if (nombreLocalidad.isEmpty() || capacidad <= 0 || precio <= 0) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos correctamente.");
            return;
        }

        // Crear una instancia de la clase Localidad con los valores ingresados por el usuario
        Localidad localidad = new Localidad();
        localidad.setTipoLocalidad(nombreLocalidad);
        localidad.setEspaciosDisponibles(capacidad);
        localidad.setPrecio(precio);

        // Cerrar el diÃ¡logo y devolver el control al formulario principal
        this.dispose();
    }//GEN-LAST:event_btonAgregarLocalidadDialogActionPerformed
